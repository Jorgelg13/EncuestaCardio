    <script type="text/javascript">
        var pregunta1 = "";
        var pregunta2 = 0;
        var pregunta3 = 0;
        var pregunta4 = 0;
        var pregunta5 = 0;
        var pregunta6 = "";
        var pregunta7 = "";

        $(function() {
            var Page = (function() {
                var $navArrows = $('#nav-arrows'),
                    $nav = $('#nav-dots > span'),
                    slitslider = $('#slider').slitslider({
                        onBeforeChange: function(slide, pos) {
                            $nav.removeClass('nav-dot-current');
                            $nav.eq(pos).addClass('nav-dot-current');
                        }
                    }),

                    init = function() {
                        initEvents();
                    },
                    initEvents = function() {
                        $navArrows.children(':last').on('click', function() {
                            slitslider.next();
                            return false;
                        });

                        $navArrows.children(':first').on('click', function() {
                            slitslider.previous();
                            return false;
                        });

                        $('#pregunta-1').change(function() {
                            pregunta1 = $('#pregunta-1').val();
                            slitslider.jump(2);
                            return false;
                        });

                        $('.preguntaDos').on('click', function(event) {
                            pregunta2 = $(this).attr('data-value');
                            var $dot = $(this);

                            if (!slitslider.isActive()) {
                                $nav.removeClass('nav-dot-current');
                                $dot.addClass('nav-dot-current');
                            }
                            slitslider.jump(3);
                            return false;

                        });

                        $('.preguntaTres').on('click', function(event) {
                            pregunta3 = $(this).attr('data-value');
                            var $dot = $(this);
                            $("#p3-1-6").css("display", "");

                            if (!slitslider.isActive()) {
                                $nav.removeClass('nav-dot-current');
                                $dot.addClass('nav-dot-current');
                            }
                            slitslider.jump(4);
                            return false;
                        });

                        $('.preguntaCuatro').on('click', function(event) {
                            pregunta4 = $(this).attr('data-value');
                            var $dot = $(this);

                            if (!slitslider.isActive()) {
                                $nav.removeClass('nav-dot-current');
                                $dot.addClass('nav-dot-current');
                            }
                            slitslider.jump(5);
                            return false;
                        });

                        $('.preguntaCinco').on('click', function(event) {
                            pregunta5 = $(this).attr('data-value');
                            var $dot = $(this);

                            if (!slitslider.isActive()) {
                                $nav.removeClass('nav-dot-current');
                                $dot.addClass('nav-dot-current');
                            }
                            slitslider.jump(6);
                            return false;
                        });

                        $('#pregunta-6').change(function() {
                            pregunta6 = $('#pregunta-6').val();
                            slitslider.jump(7);
                            return false;
                        });

                        $('#pregunta-7').change(function() {
                            pregunta7 = $('#pregunta-7').val();
                            slitslider.jump(8);
                            return false;
                        });
                    };
                return {
                    init: init
                };
            })();
            Page.init();
        });

        $("#enviar").click(function() {
            $.post("/api/v1/encuesta/guardar", {
                "pregunta1": pregunta1,
                "pregunta2": pregunta2,
                "pregunta3": pregunta3,
                "pregunta4": pregunta4,
                "pregunta5": pregunta5,
                "pregunta6": pregunta6,
                "pregunta7": pregunta7
            }, ).done(function() {
                setTimeout(
                    function() {
                        window.location.reload();
                    }, 2500);
            }).fail(function() {
                setTimeout(
                    function() {
                        window.location.reload();
                    }, 2500);
            })
        });
    </script>
